# Boolean operators
x = 41
if x == 42: # equals
        print ("equals")
if x != 42: # not equal
        print ("not equals")
if x > 42: # greater than
        print ("greater than")
if x >= 42: # greater than or equal to
        print ("greater than or equal to")
if x < 42: # less than
        print ("less than")
if x <= 42: # less than or equal to
        print ("less than or equal to")
